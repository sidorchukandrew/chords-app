export function parse(colorToParse) {
  let stripped = colorToParse
    .replace(/^(rgb|rgba)\(/, '')
    .replace(/\)$/, '')
    .replace(/\s/g, '');

  let brokenUp = stripped.split(',');
  return {r: brokenUp[0], g: brokenUp[1], b: brokenUp[2], a: brokenUp[3]};
}
export function combine({r, g, b, a}) {
  return `rgba(${r}, ${g}, ${b}, ${a})`;
}
